package lesson_3;

public class ComperisionOperators {

    public static void main(String[] args) {

        System.out.println("5 == 5 ==>"  +  (5==5));
        System.out.println("5 == 6 ==> "  + (5==6));


        System.out.println("5 == " + 5 + "==>" + (5!=5));
        System.out.println("5 == " + 5 + "==>" +  (5!=6));

        System.out.println("5 > " + 6 + "==>" + (5>6));
        System.out.println("5 < " + 5 + "==>" +  (5<6));

        System.out.println("5 >= " + 6 + "==>" + (5>=6));
        System.out.println("5 <= 6 ==>  "   +  (5<=6));




    }
}
