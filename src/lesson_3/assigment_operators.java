package lesson_3;

public class assigment_operators {
    public static void main(String[] args) {

        var a=30; //optional decloration of variable//

       a = 50;




        a+=5; //тоже самое как a=a+5//
        System.out.println("a = " +a);


        a-=5; //тоже самое как a=a-5//
        System.out.println("a = "+a);


        a*=5; //тоже самое как a=a*5//
        System.out.println("a = "+a);


        a/=5; //тоже самое как a=a/5//
        System.out.println("a = "+a);


        a%=5; //тоже самое как a=a%5//
        System.out.println("a = "+a);

    }
}
