package lesson_3;

public class increment_decrement {
    public static void main(String[] args) {


          int a =10;

        System.out.println("a=  " +a) ;

      //  a=a+1;
      //  a+=1;
        //increment//
        System.out.println("a++ =" + a++);
        System.out.println("a++ =" + a);
        System.out.println("a++ =" + ++ a); //это как 11+a//
        System.out.println("a++ =" + a);


         //decrement//
        System.out.println("a=  " +a) ;

        //  a=a-1;
        //  a-=1;

        System.out.println("a-- =" + a--);
        System.out.println("a =" + a);
        System.out.println("--a =" + -- a); //это как 11+a//
        System.out.println("a =" + a);



    }
}
