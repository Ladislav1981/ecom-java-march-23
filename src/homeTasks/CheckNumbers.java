package homeTasks;

public class CheckNumbers {
    public static void main(String[] args) {

        //Целые числа которые делятся на 2 без остатка считаются четными
        //Все числа которые делятся на 2 с остатком считаются не четными.//


        System.out.println("Modulo of 10%2->>>" + (10%2==0));

        System.out.println("Modulo of 25%2->>> "+ (25%2==0));

        System.out.println("Modulo of 55%2->>> "+(55%2==0));

        System.out.println("Modulo of 22%2->>>"+(22%2==0));

        System.out.println(" Modulo of 0%2->>>" + (0%2==0));


    }
}
